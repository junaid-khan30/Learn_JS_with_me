// leftshift operator  
console.log(5<<2);
console.log(5<<-2);
// rightshift operator
console.log(5>>2);
console.log(5>>-2);
console.log(-5>>2);
console.log(-5>>>2);

//unsigned right shift operator 
console.log(`-5>>>2`);

//bitwise AND operator 
console.log(`bitwise AND : ${7 & 5}`);
console.log(`bitwise AND : ${-6 & -2}`);

//bitwise OR operator 
console.log(`bitwise OR : ${7 | 5}`);
console.log(`bitwise OR : ${-6 | -2 }`);

//Logical AND Operator (&&)
console.log(`Logical AND : ${7 && 5}`);
console.log(`Logical AND : ${-6 && -3}`);

//Logical OR Operator (||)
console.log(`Logical OR : ${9 || 8 || 10}`);
console.log(`Logical OR : ${8 || 7 || 9}`);